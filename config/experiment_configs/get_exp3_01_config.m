function exp_config = get_exp3_01_config()
    %% Phase 3-1: v3 Sweet Spot 탐색
    
    % Schemes
    exp_config.schemes = [0, 3];
    exp_config.scheme_names = {'Baseline', 'v3-EMA'};
    
    % Sweep 변수
    exp_config.sweep_var1 = 'L_cell';
    exp_config.sweep_range1 = [0.15, 0.3, 0.5];
    
    exp_config.sweep_var2 = 'numRU_RA';
    exp_config.sweep_range2 = [1, 2, 4];
    
    exp_config.sweep_var3 = 'num_STAs';
    exp_config.sweep_range3 = [5, 10, 20];  % ⭐ 10→5로 축소 (테스트용)
    
    % 반복 횟수
    exp_config.num_runs = 5;
    
    % ⭐ 고정 파라미터 (Traffic + v3만 지정)
    exp_config.fixed = struct(...
        'numRU_SA', 8, ...
        'alpha', 1.5, ...
        'rho', 0.5, ...
        'mu_on', 0.05, ...
        'v3_EMA_alpha', 0.1, ...
        'v3_max_reduction', 0.7, ...
        'v3_sensitivity', 1e-4, ...
        'simulation_time', 10, ...
        'warmup_time', 0, ...
        'verbose', false, ...
        'collect_bsr_trace', false ...
    );
    % ⚠️ PHY 관련 필드(data_rate_per_RU, BW, etc.)는 절대 포함하지 말 것!
    
    % 메타 정보
    exp_config.total_simulations = length(exp_config.schemes) * ...
        length(exp_config.sweep_range1) * ...
        length(exp_config.sweep_range2) * ...
        length(exp_config.sweep_range3) * ...
        exp_config.num_runs;
    
    exp_config.description = 'v3 Sweet Spot 탐색 (Baseline 대비 개선율)';
end